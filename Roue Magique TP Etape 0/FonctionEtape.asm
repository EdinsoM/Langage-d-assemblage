Allume_LED	PROC
	
		PUSH 	{R2,R4}
		LDR 	R2, =0x40010C10
		MOV		R4, #(0x01 << 10) 
		STRH 	R4, [R2]
		POP 	{R2,R4}
		BX 		LR
		
		ENDP
		
Eteint_LED	PROC
	
		PUSH 	{R2,R4}
		LDR 	R2, =0x40010C14
		MOV		R4, #(0x01 << 10) 
		STRH 	R4, [R2]
		POP 	{R2,R4}
		BX 		LR
		
		ENDP




